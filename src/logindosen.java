    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            Connection DB = config.getConnection();
            Statement st = DB.createStatement();
            ResultSet rs = st.executeQuery("select * FROM dosen WHERE username = '"+ 
                    getusername.getText()+"';");
            if(rs.next()){
                if(getpassword.getText().equals(rs.getString("password"))){
                   new Jadwal(rs.getInt("dosen_id")).setVisible(true);
                   idDosen = rs.getInt("dosen_id");
                   this.dispose();
                    
                    
                }else{
                    JOptionPane.showMessageDialog(null, "Username atau password yang anda masukkan salah",
                           "unsuccesfull login", JOptionPane.ERROR_MESSAGE);
                }
            }
                
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    
    
